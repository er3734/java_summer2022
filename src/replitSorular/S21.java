package replitSorular;

public class S21 {
    public static void main(String[] args) {

        char kar='a';

        switch (kar){
            case 'a':
                System.out.println("talabeniz işleniyor");
                break;
            case 'b':
                System.out.println("yine de ilgilendiğiniz için yeşekkür ederiz");
                break;
            case 'c':
                System.out.println("Üzgünüz, şu anda herhangi bir yardım yok");
                break;
            default:
                System.out.println("Gecersiz giriş lutfen tekrar deneyin!");
        }
    }
}
